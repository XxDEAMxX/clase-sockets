package uptc.edu.co.pojos;

public class Square {
    private double x;
    private double y;

    public Square(){
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getX() {
        return x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getY() {
        return y;
    }
}
